// ฟังก์ชันสำหรับ "แก้ไขข้อมูล" (ปุ่มที่ใช้แก้ไขข้อมูลของสิ่งของที่พบ)
function editInfo() {
  // ใช้ confirm เพื่อแสดงข้อความยืนยัน
  const confirmation = confirm("คุณต้องการไปหน้าแก้ไขข้อมูลหรือไม่?");
  
  if (confirmation) {
    // ถ้าผู้ใช้เลือก OK (ยืนยัน)
    alert("ยืนยัน");
    // ที่นี่คุณสามารถเพิ่มฟังก์ชันที่เปิดฟอร์มการแก้ไขข้อมูลหรือทำงานอื่นๆ
    // เช่นการเปิด modal หรือเปลี่ยนหน้าไปที่ฟอร์มแก้ไขข้อมูล
  } else {
    // ถ้าผู้ใช้เลือก Cancel (ยกเลิก)
    alert("ยกเลิก");
  }
}